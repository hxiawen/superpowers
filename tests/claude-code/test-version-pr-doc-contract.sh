assert_has() {
    local file="$1"
    local pattern="$2"
    local name="$3"
    if rg -n "$pattern" "$file" >/dev/null 2>&1; then
        echo "  [PASS] $name"
    else
        echo "  [FAIL] $name"
        echo "  Missing pattern: $pattern"
        echo "  File: $file"
        exit 1
    fi
}

